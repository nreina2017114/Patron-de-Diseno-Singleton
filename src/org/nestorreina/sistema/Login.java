    // Poner una imagen de fondo de Login que se pueda establecer el tama√±o que guste

    class FondoPanel extends JPanel
    {
        private Image image;
        
        @Override
        public void paint(Graphics a)
        {
            image = new ImageIcon(getClass().getResource("/org/nestorreina/imagenes/fondologin.jpg")).getImage();
            
            a.drawImage(image,0, 0, getWidth(), getHeight(),this);
            
            setOpaque(false);
            
            super.paint(a);
            
        }
    }
